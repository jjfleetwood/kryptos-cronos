// Generates src/stages-meta.generated.ts — self-contained, client-safe stage
// metadata (no stage `info`/`ctf`/`quiz` content) so client bundles never pull
// the heavy stages.ts barrel (~10 MB of content). Run after changing any stage
// or epoch content:  npm run gen:meta -w @kryptos/core
//
// `--check` mode regenerates in-memory and fails (exit 1) if the committed file
// is stale — wired into CI so the metadata can't silently drift from content.
import fs from "node:fs";
import path from "node:path";
import { execSync } from "node:child_process";
import { createRequire } from "node:module";
import { fileURLToPath } from "node:url";

const require = createRequire(import.meta.url);
const __dirname = path.dirname(fileURLToPath(import.meta.url));
const CORE = path.join(__dirname, "..");
const TMP = path.join(CORE, ".tmp-meta");
const OUT = path.join(CORE, "src", "stages-meta.generated.ts");
const CHECK = process.argv.includes("--check");

// Fields that are safe + needed for client listing/sorting — must mirror the
// StageMeta type in stages-meta.ts. Everything else (info/ctf/quiz) is dropped.
const META_FIELDS = [
  "id", "epochId", "order", "title", "xp", "badge", "challengeType",
  "cveId", "wonder", "group", "easeScore", "valueScore", "rank",
];

function build() {
  execSync(
    `npx tsc -p "${path.join(CORE, "tsconfig.json")}" --outDir "${TMP}" ` +
      `--module commonjs --moduleResolution node --noEmit false --declaration false`,
    { stdio: "inherit", cwd: CORE }
  );
  const { stages, epochs } = require(path.join(TMP, "stages.js"));

  const meta = stages.map((s) => {
    const o = {};
    for (const k of META_FIELDS) if (s[k] !== undefined) o[k] = s[k];
    return o;
  });

  const banner =
    "// AUTO-GENERATED by scripts/gen-stages-meta.mjs — DO NOT EDIT BY HAND.\n" +
    "// Regenerate with:  npm run gen:meta -w @kryptos/core\n" +
    "// Self-contained metadata (no stage content) — keeps the heavy stages.ts\n" +
    "// barrel out of client bundles. Consumed via stages-meta.ts.\n";

  const src =
    banner +
    'import type { EpochConfig } from "./types";\n' +
    'import type { StageMeta } from "./stages-meta";\n\n' +
    `export const stagesMeta: StageMeta[] = ${JSON.stringify(meta)};\n\n` +
    `export const epochs: EpochConfig[] = ${JSON.stringify(epochs)};\n`;

  fs.rmSync(TMP, { recursive: true, force: true });
  return { src, count: meta.length, epochCount: epochs.length };
}

const { src, count, epochCount } = build();

if (CHECK) {
  const current = fs.existsSync(OUT) ? fs.readFileSync(OUT, "utf8") : "";
  if (current !== src) {
    console.error(
      "✗ stages-meta.generated.ts is STALE. Run `npm run gen:meta -w @kryptos/core` and commit."
    );
    process.exit(1);
  }
  console.log(`✓ stages-meta.generated.ts up to date (${count} stages / ${epochCount} epochs).`);
} else {
  fs.writeFileSync(OUT, src);
  console.log(`✓ wrote ${OUT}  (${count} stages / ${epochCount} epochs).`);
}
